package isdcm.restapp.models;

/**
 *
 * @author david
 */
public class Period {

    private String start;
    private String end;

    public Period() {
    }

    public String getStart() {
        return start;
    }

    public void setStart(String start) {
        this.start = start;
    }

    public String getEnd() {
        return end;
    }

    public void setEnd(String end) {
        this.end = end;
    }
    
}
